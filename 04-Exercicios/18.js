function numeral(numero) {
    switch (numero) {
        case 0:
            return 'Numero Zero'
        case 1:
            return 'Numero Um'
        case 2:
            return 'Numero Dois'
        case 3:
            return 'Numero Tres'
        case 4:
            return 'Numero Quatro'
        case 5:
            return 'Numero Cinco'
        case 6:
            return 'Numero Seis'
        case 7:
            return 'Numero Sete'
        case 8:
            return 'Numero Oito'
        case 9:
            return 'Numero Nove'
        case 10:
          return 'Numero Dez'
        default: 
            return 'Numero Invalido'
    }
}

